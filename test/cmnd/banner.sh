#!/bin/bash

# A~Z のアスキーアートを定義
declare -A ascii_art=(
    [A]="  #  \n # # \n#####\n#   #\n#   #"
    [B]="#### \n#   #\n#### \n#   #\n#### "
    [C]=" ### \n#   #\n#    \n#   #\n ### "
    [D]="#### \n#   #\n#   #\n#   #\n#### "
    [E]="#####\n#    \n###  \n#    \n#####"
    [F]="#####\n#    \n###  \n#    \n#    "
    [G]=" ### \n#    \n#  ##\n#   #\n ### "
    [H]="#   #\n#   #\n#####\n#   #\n#   #"
    [I]=" ### \n  #  \n  #  \n  #  \n ### "
    [J]="  ###\n   # \n   # \n#  # \n ##  "
    [K]="#   #\n#  # \n###  \n#  # \n#   #"
    [L]="#    \n#    \n#    \n#    \n#####"
    [M]="#   #\n## ##\n# # #\n#   #\n#   #"
    [N]="#   #\n##  #\n# # #\n#  ##\n#   #"
    [O]=" ### \n#   #\n#   #\n#   #\n ### "
    [P]="#### \n#   #\n#### \n#    \n#    "
    [Q]=" ### \n#   #\n#   #\n#  ##\n ####"
    [R]="#### \n#   #\n#### \n#  # \n#   #"
    [S]=" ### \n#    \n ### \n    #\n ### "
    [T]="#####\n  #  \n  #  \n  #  \n  #  "
    [U]="#   #\n#   #\n#   #\n#   #\n ### "
    [V]="#   #\n#   #\n#   #\n # # \n  #  "
    [W]="#   #\n#   #\n# # #\n## ##\n#   #"
    [X]="#   #\n # # \n  #  \n # # \n#   #"
    [Y]="#   #\n # # \n  #  \n  #  \n  #  "
    [Z]="#####\n   # \n  #  \n #   \n#####"
)

# 入力を受け取る
input="${1^^}"  # 入力を大文字に変換

# 各行ごとにアスキーアートを構築
for ((line=0; line<5; line++)); do
    row=""
    for ((i=0; i<${#input}; i++)); do
        char="${input:i:1}"
        row+="$(echo -e "${ascii_art[$char]}" | sed -n "$((line+1))p") "
    done
    echo "$row"
done
